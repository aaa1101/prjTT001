    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jRadioButton1.isSelected()){
            ClientView cli = new ClientView(this, true);
            cli.setUpdate(false);
            cli.setVisible(true);
            reload(1);
        }
        else if(jRadioButton2.isSelected()){
            if(Controller.getClientSelected() != null){
                AnimalView ani = new AnimalView(this, true);
                ani.setUpdate(false);
                ani.setVisible(true);
                reload(2);
            }
            else{
                JOptionPane.showMessageDialog(null, "É preciso escolher um dono para cadastrar um novo animal!");
            }
        }
        else if(jRadioButton3.isSelected()){
            SpeciesView spe = new SpeciesView(this, true);
            spe.setUpdate(false);
            spe.setVisible(true);
            reload(3);
        }
        else if(jRadioButton4.isSelected()){
            VetView vet = new VetView(this, true);
            vet.setUpdate(false);
            vet.setVisible(true);
            reload(4);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

