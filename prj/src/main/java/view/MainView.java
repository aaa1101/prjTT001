    private void reload(int type){
        switch(type){
            case 1:
                Controller.setTableModel(new ClientTableModel(Controller.getAllClient()));
                break;
            case 2:
                if(Controller.getClientSelected() != null)
                    Controller.setTableModel(new AnimalTableModel(Controller.getAnimalByIdClient(Controller.getClientSelected().getId())));
                else
                    Controller.setTableModel(new AnimalTableModel(Controller.getAllAnimal()));
                break;
            case 3:
                Controller.setTableModel(new SpeciesTableModel(Controller.getAllSpecies()));
                break;
            case 4: 
                Controller.setTableModel(new VetTableModel(Controller.getAllVet()));
                break;
        }
    }

