    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String[] option = {"Confirmar", "Cancelar"};
        
        if(jRadioButton1.isSelected() && Controller.getClientSelected() != null){
            int i = JOptionPane.showOptionDialog(null, "Ao excluir este cliente todos os registros dos animais de quem é dono também serão excluidos.\nDeseja continuar?", "Excluir cliente", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, "Cancelar");
        
            if(i == 0){
                Controller.deleteClient();
                reload(1);
            }
        }
        else if(jRadioButton2.isSelected() &&  Controller.getAnimalSelected() != null){
            int i = JOptionPane.showOptionDialog(null, "Deseja apagar este animal?", "Excluir animal", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, "Cancelar");
        
            if(i == 0){
                Controller.deleteAnimal();
                reload(2);
            }
        }
        else if(jRadioButton3.isSelected() &&  Controller.getSpeciesSelected() != null){
            int i = JOptionPane.showOptionDialog(null, "Ao excluir esta especie todos os animais desta especie também serão apagados.\nDeseja continuar?", "Excluir especie", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, "Cancelar");
        
            if(i == 0){
                Controller.deleteSpecies();
                reload(3);
            }
        }
        else if(jRadioButton4.isSelected() &&  Controller.getVetSelected() != null){
            int i = JOptionPane.showOptionDialog(null, "Deseja apagar este veterinario?", "Excluir cliente", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, "Cancelar");
        
            if(i == 0){
                Controller.deleteVet();
                reload(4);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione quem exclur!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

