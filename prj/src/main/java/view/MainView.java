    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if(jList1.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Selecione a consulta que deseja excluir!");
            return;
        }
        
        String[] option = {"Confirmar", "Cancelar"};
        int i = JOptionPane.showOptionDialog(null, "Tem certeza que deseja excluir?", "Excluir consulta", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, "Cancelar");

        if(i == 0){
            int selected = jList1.getSelectedIndex();
            ((DefaultListModel) jList1.getModel()).remove(selected);
            Controller.deleteAppointment(Controller.getAppointment(selected).getId());
        }
        
        
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        int id_appointment = Controller.getAppointment(jList1.getSelectedIndex()).getId();
        int id_animal = Controller.getAppointment(jList1.getSelectedIndex()).getIdAnimal();
        
        if(evt.getClickCount() == 2){
            if(Controller.getAnimalSelected() == null || Controller.getAnimalSelected().getId() != id_animal)
                Controller.setSelected(Controller.getAnimalById(id_animal));

            AppointmentView av = new AppointmentView(this, true);
            av.setAppointment(id_appointment);
            av.setVisible(true);
            
            Controller.setListAppointmentsByIdAnimal();
        }
        else{
            Controller.setTextsFieldByIdAnimal(id_animal);
        }
    }//GEN-LAST:event_jList1MouseClicked

