    public void setAppointment(int id){
        id_appointment = id;
        jTextField6.setText(Controller.getVetById(Controller.getAppointmentById(id).getIdVet()).getName());
        jTextArea2.setText(Controller.getAppointmentById(id).getNote());
        
        setExam();
    }
    
    private void setExam(){
        if(Controller.getExamByAppointment(id_appointment) != null){
            jTextField7.setText(Controller.getExamByAppointment(id_appointment).getDescr());
            jButton3.setVisible(true);
        }else{
            jTextField7.setText("");
            jButton3.setVisible(false);
        }
    }
    
