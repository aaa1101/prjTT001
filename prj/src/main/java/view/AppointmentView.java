    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ExamView ex = new ExamView((Frame) this.getOwner(), true);
        ex.setIdAppointment(id_appointment);
        
        if(jTextField7.getText().equals(""))
            ex.setUpdate(false);
        else
            ex.setUpdate(true);
        
        ex.setVisible(true);
        setExam();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String[] option = {"Confirmar", "Cancelar"};
        int i = JOptionPane.showOptionDialog(null, "Tem certeza que deseja finalizar o atendimento?", "Finalizar atendimento", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, "Cancelar");

        if(i == 0){
            Controller.finishAppointment(id_appointment, jTextArea2.getText());
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String[] option = {"Confirmar", "Cancelar"};
        int i = JOptionPane.showOptionDialog(null, "Tem certeza que deseja excluir a solicitação para este exame?", "Excluir exame", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, option, "Cancelar");
        
        if(i == 0){
            Controller.deleteExam(Controller.getExamByAppointment(id_appointment).getId());
            setExam();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

